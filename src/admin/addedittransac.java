    private void book_labelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_book_labelMouseClicked
         if (userid.getText().trim().isEmpty() ||
        author.getText().trim().isEmpty() ||
        quantity.getText().trim().isEmpty()) {

        JOptionPane.showMessageDialog(this,
            "Please fill in Title, Author, and Quantity.",
            "Required Fields", JOptionPane.WARNING_MESSAGE);
        return;
    }

    conf con = new conf();
    String sql;
    boolean success;

    String titleVal    = userid.getText().trim();     // ✅ title JTextField
    String authorVal   = author.getText().trim();
    String genreVal    = genre.getText().trim();
    String quantityVal = quantity.getText().trim();
    String statusVal   = jComboBox1.getSelectedItem().toString();

    if (isEditMode) {
        sql = "UPDATE tbl_books SET "
            + "title=?, author=?, genre=?, quantity=?, status=? "
            + "WHERE bok_id=?";
        success = con.executeUpdate(sql,
            titleVal, authorVal, genreVal, quantityVal, statusVal,
            edit_bok_id);
    } else {
        sql = "INSERT INTO tbl_books (title, author, genre, quantity, status) "
            + "VALUES (?, ?, ?, ?, ?)";
        success = con.executeUpdate(sql,
            titleVal, authorVal, genreVal, quantityVal, statusVal);
    }

    if (success) {
        JOptionPane.showMessageDialog(this,
            isEditMode ? "Book updated successfully!" : "Book added successfully!",
            "Success", JOptionPane.INFORMATION_MESSAGE);
        clearAllFields();
        dispose();
    } else {
        JOptionPane.showMessageDialog(this,
            "Operation failed. Please try again.",
            "Error", JOptionPane.ERROR_MESSAGE);
    
    }
    }//GEN-LAST:event_book_labelMouseClicked

