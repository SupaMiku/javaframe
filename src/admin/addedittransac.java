    private void loadUsersDropdown() {
        conf con = new conf();
        java.sql.Connection conn = conf.connectDB();
        try {
            java.sql.PreparedStatement ps = conn.prepareStatement(
                "SELECT a_id, name FROM tbl_acc WHERE status = 'Active'");
            java.sql.ResultSet rs = ps.executeQuery();
            user_combo.removeAllItems();
            user_combo.addItem("-- Select User --");
            while (rs.next()) {
                user_combo.addItem(rs.getInt("a_id") + " - " + rs.getString("name"));
            }
        } catch (Exception e) {
            System.out.println("Error loading users: " + e.getMessage());
        }
    }
    private void loadBooksDropdown() {
        java.sql.Connection conn = conf.connectDB();
        try {
            java.sql.PreparedStatement ps = conn.prepareStatement(
                "SELECT bok_id, title FROM tbl_books WHERE status = 'Available'");
            java.sql.ResultSet rs = ps.executeQuery();
            book_combo.removeAllItems();
            book_combo.addItem("-- Select Book --");
            while (rs.next()) {
                book_combo.addItem(rs.getInt("bok_id") + " - " + rs.getString("title"));
            }
        } catch (Exception e) {
            System.out.println("Error loading books: " + e.getMessage());
        }
    }

    

